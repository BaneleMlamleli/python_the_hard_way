"""
Accessing Elements of a List
"""
animals = ["bear", "python3.6", "peacock", "kangaroo", "whale", "platypus"]
print(f"The animal at position 1: {animals[1]}")
print(f"The third animal {animals[2]}")
print(f"The first (1st) animal: {animals[0]}")
print(f"The animal at 3: {animals[3]}")
print(f"The fifth (5th) animal: {animals[4]}")
print(f"The animal at 2: {animals[2]}")
print(f"The sixth (6th) animal: {animals[5]}")
print(f"The animal at 4: {animals[4]}")
